<?php

$location_settings = get_option('ct_location_settings');
isset($location_settings['orderby']) ? $orderby = $location_settings['orderby'] : $orderby = null;
isset($location_settings['order']) ? $order = $location_settings['order'] : $order = null;

if(empty($orderby)) $orderby = 'date';
if(empty($order)) $order = 'DESC';

global $post;

$paged = (get_query_var('paged')) ? get_query_var('paged') : 1;

if($orderby == 'views'):
	$args=array(
		'post_type' => 'ct_location',
		'post_status' => 'publish',
		'paged' => $paged,
		'meta_key' => 'Views',
		'orderby' => 'meta_value_num',
		'order' => $order,
		'location_tag' => get_query_var('location_tag'),
	);
else:
	$args=array(
		'post_type' => 'ct_location',
		'post_status' => 'publish',
		'paged' => $paged,
		'orderby' => $orderby,
		'order' => $order,
		'location_tag' => get_query_var('location_tag'),
	);
endif;
		
$query = null;
$query = new WP_Query($args);

$i = 0;

if($query->have_posts()): while($query->have_posts()): $query->the_post();

$i++;

$img_atts = array(
	'alt'	=> trim(strip_tags($post->post_title)),
	'title'	=> trim(strip_tags($post->post_title)),
);
?>
<div <?php if($i == 1): echo post_class('post ct_location first'); else: post_class('post ct_location'); endif; ?>>
	<h2><a href="<?php the_permalink(); ?>" title="<?php printf(esc_attr__('Permalink to %s', 'churchthemes'), the_title_attribute('echo=0')); ?>" rel="bookmark"><?php the_title(); ?></a></h2>
	<div class="excerpt">
		<div class="image"><a href="<?php the_permalink(); ?>" title="<?php the_title_attribute('echo=0'); ?>" rel="bookmark"><?php echo get_the_post_thumbnail($post->ID, 'archive', $img_atts); ?></a></div>
		<?php ct_location_address(); ?>
		<p><?php the_excerpt(); ?><p>
		<?php ct_location_services(); ?>
	</div>
	<div class="clear"></div>
</div>
<?php endwhile; else: ?>
<div class="post ct_location first">
	<h2><?php _e('No results found', 'churchthemes'); ?></h2>
	<p><?php _e('Sorry, nothing was found matching that criteria.', 'churchthemes'); ?></p>
</div>
<?php endif; ?>
<?php if($query->max_num_pages > 1): pagination($query->max_num_pages); endif; wp_reset_query(); ?>